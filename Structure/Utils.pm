#------------------------------------------------------------------------------
package CSS::Structure::Utils;
#------------------------------------------------------------------------------

# Pragmas.
use base qw(Exporter);
use strict;
use warnings;

# Modules.
use Error::Simple::Multiple qw(err);
use Readonly;

# Constants.
Readonly::Array our @EXPORT_OK => qw(check_params);

# Version.
our $VERSION = 0.01;

#------------------------------------------------------------------------------
sub check_params {
#------------------------------------------------------------------------------
# Check parameters.

	my ($self, @params) = @_;
	while (@params) {
		my $key = shift @params;
		my $val = shift @params;
		if (! exists $self->{$key}) {
			err "Unknown parameter '$key'.";
		}
		$self->{$key} = $val;
	}
	return;
}

1;
